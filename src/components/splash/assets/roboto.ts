function correctGlyph(fontData: FontData, glyphs: {
  [key: string]: Omit<Glyph, '_cachedOutline'>
}): FontData {
  return {
    ...fontData,
    glyphs: Object.entries(glyphs).reduce((acc, [key, value]) => ({
      ...acc,
      [key]: {
        ...value,
        _cachedOutline: value.o.split(' '),
      }
    }), {}),
  }
}

